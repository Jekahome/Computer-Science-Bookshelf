library IEEE;
use IEEE.STD_LOGIC_1164.all;

entity adder8bit is
  port (
    cin_in  : in  std_logic;
    a       : in  std_logic_vector(7 downto 0);
    b       : in  std_logic_vector(7 downto 0);
    output  : out std_logic_vector(7 downto 0);
    cin_out : out std_logic
  );
end entity adder8bit;

architecture Structural of adder8bit is
  -- Уровень 0: Базовые сигналы Generate (g0) и Propagate (p0)
  signal g0, p0 : std_logic_vector(7 downto 0);
  
  -- Уровень 1: Первая строка префиксного дерева (Сдвиг / Шаг = 1)
  signal g1, p1 : std_logic_vector(7 downto 0);
  
  -- Уровень 2: Вторая строка префиксного дерева (Сдвиг / Шаг = 2)
  signal g2, p2 : std_logic_vector(7 downto 0);
  
  -- Уровень 3: Третья строка префиксного дерева (Сдвиг / Шаг = 4)
  signal g3     : std_logic_vector(7 downto 0);
  
  -- Сигналы истинных переносов, поступающих на входы разрядов
  signal c      : std_logic_vector(7 downto 0);

begin

  ------------------------------------------------------------------------------
  -- ЭТАП 1. Препроцессинг (Входной уровень)
  ------------------------------------------------------------------------------
  gen_p0_g0: for i in 0 to 7 generate
    g0(i) <= a(i) and b(i);
    p0(i) <= a(i) xor b(i);
  end generate;

  ------------------------------------------------------------------------------
  -- ЭТАП 2. Префиксное дерево Когге-Стоуна (3 уровня)
  ------------------------------------------------------------------------------
  
  -- === УРОВЕНЬ 1 (Шаг = 1) ===
  -- Бит 0 не имеет соседа слева внутри дерева, поэтому p1(0) остается прежним
  g1(0) <= g0(0);
  p1(0) <= p0(0);
  
  -- Остальные биты (1..7) объединяются с непосредственным соседом (i-1)
  gen_l1: for i in 1 to 7 generate
    g1(i) <= g0(i) or (p0(i) and g0(i-1));
    p1(i) <= p0(i) and p0(i-1);
  end generate;


  -- === УРОВЕНЬ 2 (Шаг = 2) ===
  -- Биты 0 и 1 не имеют соседей на расстоянии 2, поэтому просто транслируются
  g2(1 downto 0) <= g1(1 downto 0);
  p2(1 downto 0) <= p1(1 downto 0);
  
  -- Остальные биты (2..7) объединяются с узлами на расстоянии 2 (i-2)
  gen_l2: for i in 2 to 7 generate
    g2(i) <= g1(i) or (p1(i) and g1(i-2));
    p2(i) <= p1(i) and p1(i-2);
  end generate;


  -- === УРОВЕНЬ 3 (Шаг = 4) ===
  -- Биты 0, 1, 2, 3 не имеют соседей на расстоянии 4, просто транслируются
  g3(3 downto 0) <= g2(3 downto 0);
  
  -- Биты 4..7 объединяются с узлами на расстоянии 4 (i-4)
  -- На этом уровне флаг 'p' нам больше не нужен, вычисляем только переносы 'g'
  gen_l3: for i in 4 to 7 generate
    g3(i) <= g2(i) or (p2(i) and g2(i-4));
  end generate;

  ------------------------------------------------------------------------------
  -- ЭТАП 3. Постпроцессинг (Интеграция cin_in и вычисление выходов)
  ------------------------------------------------------------------------------
  
  -- Интегрируем входной перенос cin_in в каждый сгенерированный префикс.
  -- Это гарантирует, что лавина cin_in мгновенно учтется в каждом разряде.
  gen_carries: for i in 0 to 7 generate
    c(i) <= g3(i) or (p2(i) and p1(i) and p0(i) and cin_in); 
  end generate;

  -- Выходной перенос сумматора — это истинный перенос самого старшего 7-го разряда
  cin_out <= c(7);

  -- Итоговая сумма: текущий Propagate (p0) XOR истинный перенос от ПРЕДЫДУЩЕГО разряда
  output(0) <= p0(0) xor cin_in;
  output(1) <= p0(1) xor c(0);
  output(2) <= p0(2) xor c(1);
  output(3) <= p0(3) xor c(2);
  output(4) <= p0(4) xor c(3);
  output(5) <= p0(5) xor c(4);
  output(6) <= p0(6) xor c(5);
  output(7) <= p0(7) xor c(6);

end architecture Structural;